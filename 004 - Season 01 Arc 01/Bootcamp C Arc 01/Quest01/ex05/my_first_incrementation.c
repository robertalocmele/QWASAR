#include <stdio.h>

int main() {
    int my_index = 0;

    // Increment my_index
    my_index++; // Now my_index is 1
    printf("%d\n", my_index);

    // Decrement my_index
    my_index -= 2; // Now my_index is -1
    printf("%d\n", my_index);

    // Increment my_index
    my_index += 3; // Now my_index is 2
    printf("%d\n", my_index);

    return 0;
}
